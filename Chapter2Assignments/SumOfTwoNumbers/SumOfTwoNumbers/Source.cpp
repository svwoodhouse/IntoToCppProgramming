/* This program puts the numbers
   62 and 99 in two integer variables and  stores 
   their sum into the total variable*/


#include <iostream>
using namespace std;

int main()
{
	int num1 = 62;
	int num2 = 99;
	int total = num1 + num2;

	cout << num1 << " + " << num2 << " = " << total;
	return 0;
}
